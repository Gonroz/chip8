use bevy::prelude::*;

pub fn keycode_to_hex(keycode: &KeyCode) -> u8 {
    match keycode {
        KeyCode::Digit1 => 0x1,
        KeyCode::Digit2 => 0x2,
        KeyCode::Digit3 => 0x3,
        KeyCode::Digit4 => 0xC,
        KeyCode::KeyQ => 0x4,
        KeyCode::KeyW => 0x5,
        KeyCode::KeyE => 0x6,
        KeyCode::KeyR => 0xD,
        KeyCode::KeyA => 0x7,
        KeyCode::KeyS => 0x8,
        KeyCode::KeyD => 0x9,
        KeyCode::KeyF => 0xE,
        KeyCode::KeyZ => 0xA,
        KeyCode::KeyX => 0x0,
        KeyCode::KeyC => 0xB,
        KeyCode::KeyV => 0xF,
        _ => 0xFF, // Returns 255 in case of one of the non-registered keys are pressed
    }
}

